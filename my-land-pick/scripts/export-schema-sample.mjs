// request_id → 색상 매핑
const reqIds = ['req-001', 'req-002', 'req-003', 'req-004', 'req-005', 'req-006', 'req-007']
const reqColorMap = Object.fromEntries(reqIds.map((id, i) => [id, groupColors[i % groupColors.length]]))

async function main() {
  const wb = new ExcelJS.Workbook()

  for (const table of tables) {
    const sheetName = table.name.length > 31 ? table.name.slice(0, 31) : table.name
    const ws = wb.addWorksheet(sheetName)

    // 1행: 컬럼 설명
    if (table.descriptions) {
      const descRow = ws.addRow(table.descriptions)
      descRow.font = { size: 9, color: { argb: 'FF374151' } }
      descRow.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FFF3F4F6' } }
      descRow.alignment = { vertical: 'top', wrapText: true }
      descRow.height = 60
    }

    // 2행: 헤더
    const headerRow = ws.addRow(table.headers)
    headerRow.font = { bold: true, size: 10, color: { argb: 'FFFFFFFF' } }
    headerRow.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF1F2937' } }
    headerRow.alignment = { vertical: 'middle', horizontal: 'center' }
    headerRow.height = 26

    // 데이터 행
    for (const rowData of table.rows) {
      const row = ws.addRow(rowData)
      row.alignment = { vertical: 'middle' }
      row.height = 22

      // request_id 기준 색상 그룹핑
      if (table.groupKey !== null) {
        const reqId = rowData[table.groupKey]
        const color = reqColorMap[reqId]
        if (color) {
          row.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: color } }
        }
      }
    }

    // 컬럼 너비
    ws.columns.forEach((col, idx) => {
      const header = table.headers[idx] || ''
      let maxLen = header.length
      for (const row of table.rows) {
        const val = String(row[idx] ?? '')
        maxLen = Math.max(maxLen, val.length)
      }
      col.width = Math.min(Math.max(maxLen * 1.4 + 2, 10), 55)
    })

    // 테두리
    ws.eachRow((row) => {
      row.eachCell((cell) => {
        cell.border = {
          top: { style: 'thin', color: { argb: 'FFD1D5DB' } },
          left: { style: 'thin', color: { argb: 'FFD1D5DB' } },
          bottom: { style: 'thin', color: { argb: 'FFD1D5DB' } },
          right: { style: 'thin', color: { argb: 'FFD1D5DB' } },
        }
      })
    })

    // 숫자 포맷
    const startRow = table.descriptions ? 3 : 2
    const moneyKeywords = ['금액', '총액', '수수료', '마크업', '수익', '지급', '수취', 'gmv', '견적가', '거래액']
    ws.eachRow((row, rowNum) => {
      if (rowNum < startRow) return
      row.eachCell((cell, colNum) => {
        const header = table.headers[colNum - 1] || ''
        if (moneyKeywords.some(k => header.includes(k)) && typeof cell.value === 'number') {
          cell.numFmt = '#,##0'
        }
        if (header.includes('수수료율') && typeof cell.value === 'number') {
          cell.numFmt = '0.0%'
        }
        if (header === '비율' && typeof cell.value === 'number') {
          cell.numFmt = '0%'
        }
      })
    })

    // 필터 + 고정 (설명행 + 헤더행 고정)
    const headerRowNum = table.descriptions ? 2 : 1
    ws.autoFilter = { from: { row: headerRowNum, column: 1 }, to: { row: headerRowNum, column: table.headers.length } }
    ws.views = [{ state: 'frozen', ySplit: headerRowNum }]
  }

  const outPath = '/Users/youngjun-hwang/Desktop/Claude/my-land-pick/결제_정산_테이블_샘플데이터.xlsx'
  await wb.xlsx.writeFile(outPath)
  console.log(`완료: ${outPath}`)
}

main()
